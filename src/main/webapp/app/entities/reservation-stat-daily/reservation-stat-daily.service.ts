import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { type IReservationStatDaily } from '@/shared/model/reservation-stat-daily.model';

const baseApiUrl = 'api/reservation-stat-dailies';

export default class ReservationStatDailyService {
  public find(id: number): Promise<IReservationStatDaily> {
    return new Promise<IReservationStatDaily>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IReservationStatDaily): Promise<IReservationStatDaily> {
    return new Promise<IReservationStatDaily>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IReservationStatDaily): Promise<IReservationStatDaily> {
    return new Promise<IReservationStatDaily>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IReservationStatDaily): Promise<IReservationStatDaily> {
    return new Promise<IReservationStatDaily>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
